class Solution:
    def search(self, nums: List[int], target: int) -> int:
        left, right = 0, len(nums) - 1
        
        while left <= right:
            # Using (left + right) // 2 works fine in Python since it handles arbitrarily large integers,
            # but left + (right - left) // 2 prevents overflow in other statically-typed languages.
            mid = left + (right - left) // 2
            
            if nums[mid] == target:
                return mid
            elif nums[mid] < target:
                left = mid + 1
            else:
                right = mid - 1
                
        return -1